package org.lightj.session;

public class FlowContextException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2703770383917147356L;

	public FlowContextException() {
		super();
	}

	public FlowContextException(String message) {
		super(message);
	}

	public FlowContextException(String message, Throwable cause) {
		super(message, cause);
	}

	public FlowContextException(Throwable cause) {
		super(cause);
	}
}
